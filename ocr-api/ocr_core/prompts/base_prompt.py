def get_system_message(multiple_images: bool = False) -> str:
    """システムメッセージを取得"""
    if multiple_images:
        return SYSTEM_MESSAGE + """

【複数画像の統合ルール - 重要】
1. 全ての画像を注意深く確認し、情報を統合して1つのJSONを返す
2. ある画像で「---」や空欄の項目でも、別の画像に値があればそちらを採用する
   - 例: 物件概要書の「地目」が「---」でも、登記簿の「地目」欄に「宅地」とあれば "宅地" を返す
   - 例: 物件概要書の「価格」が「--- 千円」でも、別の画像に価格があればそちらを採用
3. 同じ項目が複数画像に存在し値が異なる場合は、より詳細・具体的な値を優先する
4. 「---」「－－－」等のプレースホルダーは「値なし」を意味する。別画像に実際の値があればそちらを使う
5. 全ての画像で値が読み取れない場合のみ null を返す
6. 【書類タイプ別の記載項目 - 重要】
   - 物件概要書・調査概要書: 用途地域、建蔽率、容積率、高度指定、防火指定、道路情報、交通情報、価格等
   - 登記簿: 地目、地積、建物構造、建物面積等
   - 容積率・高度指定・防火指定は登記簿には記載されない。物件概要書にのみ記載される
   - ある画像に特定の項目がないのは正常。他の画像から抽出できていれば「確認できない」としないこと"""
    return SYSTEM_MESSAGE


def get_common_rules(multiple_images: bool = False) -> str:
    """共通ルールを取得"""
    rules = COMMON_RULES
    if multiple_images:
        rules += """

■ 複数画像の統合（最重要）
- 1枚目で「---」や空欄の項目は、2枚目・3枚目に値がないか必ず確認する
- 物件概要書と登記簿など、異なる書類に同じ項目が記載されている場合がある
- 「---」は「この書類には記載なし」の意味であり、「値が存在しない」ではない
- 全画像を確認した上で、いずれの画像にも値がない場合のみ null とする
- 書類タイプによって記載項目が異なる:
  物件概要書: 容積率、建蔽率、用途地域、高度指定、防火指定、道路、交通、価格
  登記簿: 地目、地積、建物構造
  → 登記簿に容積率・高度指定がないのは正常。物件概要書から抽出済みなら値を保持すること"""
    return rules


def get_common_fields() -> str:
    """共通フィールド定義を取得"""
    return COMMON_FIELDS
