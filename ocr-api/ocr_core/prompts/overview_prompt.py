"""
物件概要用プロンプト
共通フィールド（base_prompt.py）+ 物件概要固有フィールドで構成
"""
from .base_prompt import get_common_rules, get_common_fields


def get_overview_prompt(image_count: int) -> str:
    """物件概要用プロンプト"""
    return f"""不動産調査資料から物件概要を抽出してください。「調査概要書」を優先。

{get_common_rules(image_count > 1)}

【フィールド定義】
{get_common_fields()}

■ 土地情報（重要 - 必ず抽出）
land_area_raw: 地積（原文のまま、例: "150.00㎡(公簿)"、"200㎡（実測）"、"計1,080.22㎡"）
  【抽出ルール】
  - 「地積」欄の「公簿面積」「公簿」「登記面積」「実測」から抽出
  - 「計」が付いている場合は合計値を抽出
  - 公簿と実測の両方がある場合は実測を優先して抽出（例: "82.53㎡（実測）"）
  - 実測がなく公簿のみの場合は公簿を抽出（例: "82.86㎡(公簿)"）
land_category: 地目（例: "宅地"、"雑種地"、"田"、"畑"、"山林"、"原野"）
  【抽出ルール - 最重要】
  - 物件概要書の「地目」欄が「---」「－－－」「記載なし」の場合でも null にしない
  - 物件概要書で地目が空欄・ダッシュの場合は、登記簿の「地目」欄から必ず抽出すること
  - 登記簿の表題部（土地の表示）に「地目」欄がある → そこから抽出
  - 複数画像がある場合、いずれかの画像に地目の記載があれば抽出する
  - 登記簿の画像解像度が低く文字が判読できない場合は null を返す（推測禁止）

■ 道路情報（重要 - 土地セクション内を確認）
【方位の読み取り - 最重要注意】
道路の方位を読む際は、必ず以下の手順で確認すること:
  1. 「側」の直前にある文字を全て読む（1文字ではなく2文字の場合がある）
  2. 「南」の直後に「西」「東」がないか確認 → あれば「南西」「南東」
  3. 「北」の直後に「西」「東」がないか確認 → あれば「北西」「北東」
  4. 2文字目がなければ「南」「北」「東」「西」の1文字方位
  ※「南西側」を「西側」と読むのは誤り。「南」を見落とさないこと
  ※「北東側」を「東側」と読むのは誤り。「北」を見落とさないこと
  ※ 「西」の左隣に「南」「北」がないか、「東」の左隣に「南」「北」がないか必ず確認すること

road_1_raw: 道路1（原文のまま）
  - 「土地」セクション内の「接面道路」「前面道路」「道路」行を探す
  - 方位 + 幅員 + 道路種別（公道/私道）
  - 8方位: 北東/北西/南東/南西/北/南/東/西
  - 例: "南西側 約5.4〜5.8m 公道"、"南東側 4.5m 私道"、"北西側 6m 公道"
road_2_raw: 道路2（2面道路の場合に抽出、なければ null）

■ 用途地域等
use_district_raw: 用途地域（原文のまま、複数ある場合はカンマ区切り）
building_coverage_ratio_raw: 建蔽率（原文のまま、例: "60%"）
floor_area_ratio_raw: 容積率（原文のまま、例: "200%"）

■ 建物情報
building_type: 建物種類（以下から最も近い1つを選択）
  居宅/共同住宅/店舗/事務所/店舗兼住宅/事務所兼住宅/店舗兼事務所/倉庫/車庫/工場
  【選択ルール】
  - 建物種類欄に1つだけ記載 → そのまま選択
  - 複数の用途が記載されている場合（例: "店舗 事務所 共同住宅"）→ 兼用表記を優先:
    - 「店舗」+「事務所」が含まれる → "店舗兼事務所"
    - 「店舗」+「住宅」/「居宅」が含まれる → "店舗兼住宅"
    - 「事務所」+「住宅」/「居宅」が含まれる → "事務所兼住宅"
    - 上記に該当しない場合 → 最初に記載されている用途を選択
  - 「一棟マンション」「アパート」→「共同住宅」
  - 「一棟ビル」「ビル」→「事務所」
  - 必ず上記リストのいずれか1つを返すこと（null にしない）
building_structure: 建物構造（原文のまま）
building_area_raw: 延べ床面積（原文。階数ごとの面積がある場合は改行区切りで全階分を含めること。例: "1階 124:86\n2階 150:00"、"1階部分 57:24\n2階部分 43:30"）
exclusive_area_raw: 専有面積（原文のまま）
construction_date_raw: 築年月（原文のまま、例: "平成10年3月"、"1998年3月竣工"）

■ 価格フラグ（boolean: true/false/null）
price_is_consultation: 価格欄に「相談」と記載されているか
price_is_unknown: 価格欄に「不明」「未定」と記載されているか
price_is_bid: 価格欄に「入札」と記載されているか
price_is_negotiation: 価格欄に「協議」と記載されているか"""
